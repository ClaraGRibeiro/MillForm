        <p><b>Créditos:</b> A escala foi construída e validada para a população portuguesa por José Pinto Lopes, Helena Santos Silva e Eva Morais da Universidade de Trás-os-Montes e Alto Douro em 2018.</p>
        <p><b>Justificativa:</b> Para ser um bom pensador crítico, é necessário possuir tanto competências quanto disposições de pensamento crítico. As competências referem-se à habilidade cognitiva para realizar tarefas, enquanto as disposições envolvem a motivação e a tendência de usar essas habilidades. As disposições são especialmente importantes para a aplicação a longo prazo do pensamento crítico, sendo consideradas como uma motivação interna para resolver problemas, avaliar ideias e tomar decisões de forma rigorosa. No questionário a seguir, serão avaliadas as disposições de pensamento crítico, dispostas em formato de escala</p>
